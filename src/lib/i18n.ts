export function resolveLanguage(
  preference: LanguagePreference,
  browserLanguage?: string
): ResolvedLanguage {
  if (preference === "en" || preference === "es") {
    return preference;
  }

  return browserLanguage?.toLowerCase().startsWith("es") ? "es" : "en";
}
